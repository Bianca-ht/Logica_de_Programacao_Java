/*
-------> Métodos:

   - System.out: representa a saída padrão, permitindo exibir dados no 
                 console quando executamos uma aplicação.
     
   - println() : gera uma string de texto, cria uma nova linha abaixo da atual.
     
   - print() gera uma string de texto.
     
   - printf(): mostra os dados na saída formatados.
     
                      - Especificadores: 

                               %s    String de caracteres
                               %d	Número inteiro decimal 
                               %u	Número inteiro decimal sem sinal
                               %o	Número inteiro octal sem sinal
                               %x, %X	Número inteiro hexadecimal sem sinal, minúsculo ou maiúsculo
                               %f	Float
                               %2f	Double
                               %e, %E	Número real, em notação científica, minúsculo ou maiúsculo
                               %b	Boolean
                               %c	Caractere (char)

                      - Caracteres:

                               \t	Tabulação
                               \b	Backspace
                               \n	Nova Linha
                               \r	Retorno de carro
                               \’	Aspa simples
                               \”	Aspa dupla
                               \\	Barra invertida        

*/
